#ifndef MYSTICSQUARE_BACKGROUNDENUM_HH
#define MYSTICSQUARE_BACKGROUNDENUM_HH

/**
 * Contains the different types of backgrounds.
 */
enum class Background {
    /**
     * The default one. Does not use a picture but instead numbered tiles are used.
     */
    NUMBERED,

    /**
     * A picture of a forest.
     */
    FOREST,

    /**
     * A picture of a tree.
     */
    TREE,

    /**
     * An artwork of a computer network.
     */
    NETWORK
};

#endif //MYSTICSQUARE_BACKGROUNDENUM_HH
